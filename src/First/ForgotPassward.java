    private void phonesignKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_phonesignKeyPressed
        // TODO add your handling code here:
        String phn = phonesign.getText();
        int l = phn.length();
        char c = evt.getKeyChar();
        if (c >= '0' && c <= '9') {
            if (l < 11) {
                phonesign.setEditable(true);
            } else {
                phonesign.setEditable(false);
            }
        } else {
            if (evt.getExtendedKeyCode() == KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode() == KeyEvent.VK_DELETE) {
                phonesign.setEditable(true);
            } else {
                phonesign.setEditable(false);
            }
        }
    }//GEN-LAST:event_phonesignKeyPressed

    public boolean isEmpty() {
        if (phonesign.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill out the Phone Number", "Invalid", 2);
            return false;
        } else if (fpass.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Enter your new Password", "Invalid", 2);
            return false;
        }
        return true;
    }

    public boolean emailValid() {
        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill out the Email Address", "Invalid", 2);
            return false;
        } else if (!jTextField1.getText().matches("^[A-Za-z0-9+_.-]+@(.+)$")) {
            JOptionPane.showMessageDialog(null, "Invalid Email Address", "Invalid", 2);
            return false;
        }
        return true;

    }
    private void jLabel9MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel9MouseClicked
        // TODO add your handling code here:
        if (emailValid()) {
            if (jRadioButton1.isSelected()) {
                if (fg.isEmailexistj(jTextField1.getText())) {

                    jTextField1.setBackground(notEdit);
                    jTextField1.setEditable(false);
                    phonesign.setBackground(Edit);
                    phonesign.setEditable(true);
                    fpass.setBackground(Edit);
                    fpass.setEditable(true);
                    jButton1.setEnabled(true);

                }
            } else {
                if (fg.isEmailexistr(jTextField1.getText())) {

                    jTextField1.setBackground(notEdit);
                    jTextField1.setEditable(false);
                    phonesign.setBackground(Edit);
                    phonesign.setEditable(true);
                    fpass.setBackground(Edit);
                    fpass.setEditable(true);
                    jButton1.setEnabled(true);

                }
            }
        }
    }//GEN-LAST:event_jLabel9MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (isEmpty()) {
            if (jRadioButton1.isSelected()) {
                String email=jTextField1.getText();
                String newans=phonesign.getText();
                if(fg.getsphn(email, newans))
                {
                    String pass=String.valueOf(fpass.getPassword());
                    fg.newspass(email, pass);
                    new Login().setVisible(true);
                    this.dispose();
                }
            } else {
                String email=jTextField1.getText();
                String newans=phonesign.getText();
                if(fg.getrphn(email, newans))
                {
                    String pass=String.valueOf(fpass.getPassword());
                    fg.newrpass(email, pass);
                    new Login().setVisible(true);
                    this.dispose();
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

