    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (email.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill out the email");
        } else if (!email.getText().matches("^[A-Za-z0-9+_.-]+@(.+)$")) {
            JOptionPane.showMessageDialog(null, "Invalid Email Address", "Invalid", 2);
        } else if (loginpass.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill out the password");
        } else {
            if (jRadioButton2.isSelected()) {
                String name = email.getText();
                String pass = String.valueOf(loginpass.getPassword());
                try {
                    Connection con = (Connection) Myconnection.getConnection();
                    PreparedStatement ps;
                    ps = con.prepareStatement("select * from admin where email =? and pass =?");
                    ps.setString(1, name);
                    ps.setString(2, pass);
                    ResultSet rs = ps.executeQuery();
                    if (rs.next()) {
                        Admin hpage = new Admin();
                        hpage.setVisible(true);
                        hpage.adname.setText("Welcome home, " + name + "!");
                        hpage.pack();
                        hpage.setLocationRelativeTo(null);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Incorrect", "login failed", 1);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else if (jRadioButton1.isSelected()) {
                String name = email.getText();
                String pass = String.valueOf(loginpass.getPassword());
                try {
                    Connection con = (Connection) Myconnection.getConnection();
                    PreparedStatement ps;
                    ps = con.prepareStatement("select * from jseeker where semail =? and spass =?");
                    ps.setString(1, name);
                    ps.setString(2, pass);
                    ResultSet rs = ps.executeQuery();
                    if (rs.next()) {
                        Jobseekertest hpage = new Jobseekertest();
                        hpage.setVisible(true);
                        hpage.pack();
                        hpage.setLocationRelativeTo(null);
                        //Jobseekertest.userEmail.setText(name);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Incorrect", "login failed", 1);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else if (jRadioButton3.isSelected()) {
                String name = email.getText();
                String pass = String.valueOf(loginpass.getPassword());
                try {
                    Connection con = (Connection) Myconnection.getConnection();
                    PreparedStatement ps;
                    ps = con.prepareStatement("select rid from recruiter where remail = ? and rpass = ?");
                    ps.setString(1, name);
                    ps.setString(2, pass);
                    ResultSet rs = ps.executeQuery();
                    if (rs.next()) {
                        int r_id = rs.getInt("rid");
                        Recruiter hpage = new Recruiter();
                        hpage.setVisible(true);
                        hpage.setRid(r_id,rs.getString("remail"));
                        hpage.pack();
                        hpage.setLocationRelativeTo(null);
                        // Set the rid in the recruiter page
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Incorrect", "Login Failed", 1);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

